# 1. 北京出租车计价器:
#   收费标准:
#     3公里以内收费13元
#     超出3公里以外，每公里基本单价2.3元/公里
#     空驶费: 超过15公里后，每公里加收 1.15元空驶费
#   要求:
#     输入公里数，打印出费用金额(以元为单位四舍五入)

km = int(input("请输入公里数: "))

# 方法1
# money = 0
# if km < 3:
