# elif 3 <= km <= 15:
#     money = 13 + (km - 3) * 2.3
# else:
#     money = 13 + (km - 3) * 2.3 + (km-15)* 1.15

# 方法2 优化方法1
money = 13
if km > 3:
    money += (km - 3) * 2.3
if km > 15:
    money += (km - 15) * 1.15

print("费用:", round(money))


